
pub const NUM_LEDS: usize = 90;
pub const TX_BUFFER_SIZE: usize = NUM_LEDS * 9 + 1;

#[derive(Clone, Copy, Default, PartialEq, Eq)]
pub struct Rgb {
	pub r: u8,
	pub g: u8,
	pub b: u8,
}

impl Rgb {
	pub const fn new(r: u8, g: u8, b: u8) -> Self {
		Self { r, g, b }
	}

	pub fn to_spi_bits(self) -> [u8; 9] {
		let r_hi = SPI_PIXEL_LUT[usize::from(self.r >> 4)];
		let r_lo = SPI_PIXEL_LUT[usize::from(self.r & 0xf)];

		let g_hi = SPI_PIXEL_LUT[usize::from(self.g >> 4)];
		let g_lo = SPI_PIXEL_LUT[usize::from(self.g & 0xf)];

		let b_hi = SPI_PIXEL_LUT[usize::from(self.b >> 4)];
		let b_lo = SPI_PIXEL_LUT[usize::from(self.b & 0xf)];

		[
			(g_hi >> 4).try_into().unwrap(),
			((g_hi << 4 & 0xff) | (g_lo >> 8)).try_into().unwrap(),
			(g_lo & 0xff).try_into().unwrap(),

			(r_hi >> 4).try_into().unwrap(), 
			((r_hi << 4 & 0xff) | (r_lo >> 8)).try_into().unwrap(),
			(r_lo & 0xff).try_into().unwrap(), 

			(b_hi >> 4).try_into().unwrap(),
			((b_hi << 4 & 0xff) | (b_lo >> 8)).try_into().unwrap(),
			(b_lo & 0xff).try_into().unwrap(),
		]
	}

	pub fn apply_gamma(self) -> Rgb {
		Rgb {
			r: GAMMA8[usize::from(self.r)],
			g: GAMMA8[usize::from(self.g)],
			b: GAMMA8[usize::from(self.b)],
		}
	}
}

#[derive(Clone, Copy, Default, PartialEq)]
pub struct Hsv {
	pub h: f32,
	pub s: f32,
	pub v: f32,
}

pub fn hsv_to_rgb_rainbow(hsv: Hsv) -> Rgb {
	let h = hsv.h;
	let s = hsv.s;
	let v = hsv.v;

	const Y1: u8 = 0;
	const Y2: u8 = 1;

	const G2: u8 = 0;

	let mut hue_byte = (h / 360.0) * 256.0;
	if hue_byte >= 256.0 { hue_byte = 0.0; }

	let hue_int: i32 = hue_byte as i32;

	let offset = (hue_int & 0x1F) as f32;

	let offset_norm = offset / 32.0;

	let third = offset_norm / 3.0;
	let two_thirds = offset_norm * 2.0 / 3.0;

	let mut r: f32 = 0.0; let mut g: f32 = 0.0; let mut b: f32 = 0.0;

	const K255: f32 = 1.0;
	const K171: f32 = 171.0 / 255.0;
	const K170: f32 = 170.0 / 255.0;
	const K85: f32 = 85.0 / 255.0;

	let band = (hue_int >> 5) & 0x07;

	match band {
		0 => {
			r = K255 - third; g = third; b = 0.0;
		},
		1 => {
			if Y1 != 0 {
				r = K171; g = K85 + third; b = 0.0;
			} else if Y2 != 0 {
				r = K170 + third; g = K85 + two_thirds; b = 0.0;
			} else {
				r = K255 - third; g = third; b = 0.0;
			}
		}
		2 => {
			if Y1 != 0 {
				r = K171 - two_thirds; g = K170 + third; b = 0.0;
			} else if Y2 != 0 {
				r = K255 - offset_norm; g = K255; b = 0.0;
			} else {
				r = K255 - third; g = third; b = 0.0;
			}
		}
		3 => { r = 0.0; g = K255 - third; b = third; }
		4 => { r = 0.0; g = K171 - two_thirds; b = K85 + two_thirds; }
		5 => { r = third; g = 0.0; b = K255 - third; }
		6 => { r = K85 + third; g = 0.0; b = K171 - third; }
		7 => { r = K170 + third; g = 0.0; b = K85 - third; }
		_ => {}
	};

	if G2 != 0 { g = g * 0.5; }
	// if Gscale != 0 { g = g * Gscale / 255.0; }
	
	if s < 1.0 {
		if s == 0.0 {
			r = 1.0; g = 1.0; b = 1.0;
		} else {
			let desat = (1.0 - s) * (1.0 - s);
			let sat_scale = 1.0 - desat;

			r = r * sat_scale + desat;
			g = g * sat_scale + desat;
			b = b * sat_scale + desat;
		}
	}

	if v < 1.0 {
		let scaled = v * v;
		r *= scaled;
		g *= scaled;
		b *= scaled;
	}

	let r8 = (r * 255.0 + 0.5).min(255.0) as u8;
	let g8 = (g * 255.0 + 0.5).min(255.0) as u8;
	let b8 = (b * 255.0 + 0.5).min(255.0) as u8;

	Rgb{ r: r8, g: g8, b: b8 }
}

pub fn fill_transfer_buffer(spi_buf: &mut [u8; TX_BUFFER_SIZE], leds: &[Rgb; NUM_LEDS]) {
	let mut idx = 0;

	for pix in leds {
		let buf = pix.to_spi_bits();
		spi_buf[idx..idx+9].copy_from_slice(&buf);
		idx += 9;
	}
	spi_buf[NUM_LEDS * 9] = 0;
}

pub fn apply_gamma(buf: &mut [Rgb; NUM_LEDS]) {
	for led in buf {
		*led = led.apply_gamma();
	}
}

